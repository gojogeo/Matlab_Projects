function [sigma, tau] = berechneSpannungen(t, X, Y, N, Q, M, par)

% (AUFGABE 5)
% =========================================================================
% =========================================================================

A = par.A;
I = par.I;

% =========================================================================

nt = length(t);
nx = length(X);
ny = length(Y);

% =========================================================================

sigma = zeros(nx, ny, nt);
tau   = zeros(nx, ny, nt);

disp('berechne Spannungen');

% =========================================================================

for i = 1:nx            % longitudinalrichtung x
    
    for j = 1:ny        % transversalrichtung y
        for k = 1:nt    % zeit t
            
            tau  (i, j, k) = (1/A)*Q(k,i);
            sigma(i, j, k) = (1/A)*N(k,i) - (M(k,i)/I)*Y(j);
            
        end % for k
    end % for j
end % for i


% =========================================================================
% =========================================================================

end % function
