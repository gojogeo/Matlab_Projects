function [x, y, xD, yD] = trafoPolarNachKartesisch(phi, phiD, par)

% (AUFGABE 2)
% =========================================================================
% =========================================================================
% Parameter einlesen

l = par.l;
b = par.b;

% =========================================================================
% =========================================================================
% Polarkoordinaten in kartesische Koordinaten umrechnen

if (nargout >= 2)                                          % Positionsebene
    
    x  = b + l*sin(phi);   
    y  = -l*cos(phi);     
    
end % if

% =========================================================================

if (nargout >= 4)                                   % Geschwindigkeitsebene
    
    xD = l*(phiD.*cos(phi)); 
    yD = l*(phiD.*sin(phi)); 
    
end % if

% =========================================================================
% =========================================================================

end % function
