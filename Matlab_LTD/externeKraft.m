function Fe = externeKraft(t, par)

% =========================================================================
% =========================================================================
% Parameter auslesen

scenario = par.szenario;

% =========================================================================

switch scenario
    
    case 'a'
        
        Fe = 0;
        
    case 'b'
        
        Fe = 0;
        
    case 'c'
        
        Fe = 0;
    
    case 'd'
        
        Fe = 0;
        
    case 'e'
        
        Fe = par.FeMax*sin(t);
        
end % switch

% =========================================================================
% =========================================================================

end % function
